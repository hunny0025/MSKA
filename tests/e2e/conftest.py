@pytest_asyncio.fixture(scope="session")
async def seeded_env():
    """
    Session-scoped fixture that:
    1. Creates an isolated SQLite database with all dummy data.
    2. Overrides the vector store and file storage paths.
    3. Returns the DummyDataSeeder instance with all entity IDs.
    """
    from sqlalchemy.ext.asyncio import create_async_engine, async_sessionmaker, AsyncSession

    # Paths for isolated test environment
    test_dir = os.path.join(PROJECT_ROOT, ".e2e_test_workspace")
    db_path = os.path.join(test_dir, "e2e_test.db")
    vector_path = os.path.join(test_dir, "vectors")
    storage_path = os.path.join(test_dir, "storage")

    # Clean slate
    if os.path.exists(test_dir):
        shutil.rmtree(test_dir)
    os.makedirs(test_dir, exist_ok=True)

    db_url = f"sqlite+aiosqlite:///{db_path}"

    # Override settings BEFORE importing anything that reads them at module level
    from core.config import get_settings
    settings = get_settings()
    settings.vector_store_path = vector_path
    settings.file_storage_path = storage_path

    # Import and run seeder
    from scripts.seed_dummy_data import DummyDataSeeder
    seeder = DummyDataSeeder(db_url, vector_path, storage_path)
    summary = await seeder.seed_all()

    # Store summary on the seeder for test access
    seeder.summary = summary
    seeder.db_url = db_url
    seeder.test_dir = test_dir

    yield seeder

    # Cleanup after all tests
    if os.path.exists(test_dir):
        shutil.rmtree(test_dir)


@pytest_asyncio.fixture(scope="session")
async def db_session(seeded_env):
    """
    Provides a read-only async session to the seeded test database.
    """
    from sqlalchemy.ext.asyncio import create_async_engine, async_sessionmaker, AsyncSession

    engine = create_async_engine(seeded_env.db_url, echo=False)
    session_factory = async_sessionmaker(engine, class_=AsyncSession, expire_on_commit=False)

    async with session_factory() as session:
        yield session

    await engine.dispose()


