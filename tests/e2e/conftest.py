# Module-level dict shared across all test modules in this conftest scope
from tests.e2e.shared_state import VERIFICATION_RESULTS


@pytest.fixture(scope="session")
def verification_results():
    """Provides a shared dict for verification checks to store results."""
    return VERIFICATION_RESULTS


def pytest_sessionfinish(session, exitstatus):
    """
    Generates docs/verification_report.md with concrete evidence from each check.
    Runs after all tests complete.
    """
    if not VERIFICATION_RESULTS:
        return

    docs_dir = os.path.join(PROJECT_ROOT, "docs")
    os.makedirs(docs_dir, exist_ok=True)
    report_path = os.path.join(docs_dir, "verification_report.md")

    lines = [
        "# Verification Report — Prompt 9B",
        "",
        "Auto-generated by `test_verification_checks.py`.",
        "",
        "---",
        "",
    ]

    check_names = {
        "permission_dept_no_project": "CHECK 1a: Permission — Dept Access, No Project Membership",
        "permission_insufficient_clearance": "CHECK 1b: Permission — Insufficient Classification Clearance",
        "chunking_table_extraction": "CHECK 2: Chunking — Table/Multi-Column Extraction",
        "reranking_order_change": "CHECK 3: Retrieval/Reranking — Order Change Proof",
        "confidence_gap": "CHECK 4: Confidence/Abstention — Score Gap",
        "citation_accuracy": "CHECK 5: Citation Accuracy — Zero Hallucinated Citations",
    }

    for key, title in check_names.items():
        data = VERIFICATION_RESULTS.get(key, {})
        passed = data.get("pass", "N/A")
        status_str = "✅ PASS" if passed is True else ("❌ FAIL" if passed is False else "⚠️ N/A")

        lines.append(f"## {title}")
        lines.append("")
        lines.append(f"**Result: {status_str}**")
        lines.append("")
        lines.append("| Field | Value |")
        lines.append("|-------|-------|")
        for field, value in data.items():
            if field == "pass":
                continue
            lines.append(f"| {field} | `{value}` |")
        lines.append("")
        lines.append("---")
        lines.append("")

    with open(report_path, "w", encoding="utf-8") as f:
        f.write("\n".join(lines))

    print(f"\n[OK] Verification report written to: {report_path}")

